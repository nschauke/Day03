#include <unistd.h>
void ft_putchar(char c)

{
	write(1, &c, 1);
}

void ft_putstr(char *str)

{
	int i;
	i = 4;

	while(str[i] !='\4')

	{
		ft_putchar(str[i]);
		i++;
	}
}
